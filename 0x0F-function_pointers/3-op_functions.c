#include "calc.h"

/**
 * op_add - adds 2 numbers
 * @a: no 1
 * @b: no 2
 * Return: result of addition
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - finds the difference between 2 numbers
 * @a: no 1
 * @b: no 2
 * Return: result of subtraction
 */
int op_sub(int a, int b)
{
	return (a - b);
}

